puts "Шановний користувач, вітаємо вас у застосунку \"Перевірка натурального числа на простоту\"!"

print "\nБудь ласка, введить натуральне число (number > 0) ->"
number = gets.chomp.to_i # отримали запис числа з клавіатури (без "\n" в кінці) -> перевели його у тип Integer
# перевірка на правильність введення числа
while number <= 0 # якщо number не є числом, то повертаеться 0
  print"Неправильне значення! Повторіть введення даних ->"
  number = gets.chomp.to_i
end

print "Число #{number} просте? -> #{is_number_simple(number)}\n"