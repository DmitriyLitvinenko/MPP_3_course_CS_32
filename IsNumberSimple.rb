# функція перевірки числа на простоту (метод пошуку дільників числа)
def is_number_simple(number)
  # оброблюємо особливий випадок для 1
  if number == 1
    return false
  end
  # лінійно перевіряємо число на усі можливі дільники - від 2 до number-1
  i = 2
  while i < number do
    if number % i == 0
      return false
    end
    i += 1
  end
  true
end

